import math # is the meth module with special math properaties

pi = 3.14159265359
x = 1
y = 2
z = 3

# round = rounds the number to the next whole int

print(round(pi))

# ceil = rounds the number UP to the next whole int

print(math.ceil(pi))

# floor = rounds the number DOWN to the next whole int

print(math.floor(pi))

# abs (absolute value) = tells how far the number is from zero

print(abs(pi))

# pow = will raise a base number to the power (To the exponent)

print(pow(pi,2))

# sqrt = will display the squareroot (doesnt work on negative numbers)

print(math.sqrt(pi))

# max = finds the highest given number

print(max(x,y,z))

# min = finds the lowest given number

print(min(x,y,z))
